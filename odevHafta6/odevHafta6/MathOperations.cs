using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace odevHafta6
{
    public class MathOperations
    {
        public int topla(int x, int y)
        {
            return x + y;
        }
        public int cıkart(int x, int y)
        {
            return x - y;
        }
        public double topla(double x, double y, double z)
        {
            return x + y + z;
        }
        public double cıkart(double x, double y, double z)
        {
            return x - y - z;
        }
      

    }
}
